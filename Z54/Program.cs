Console.Write("Введите количество строк массива m: ");
int m = int.Parse(Console.ReadLine()!);

Console.Write("Введите количество столбцов массива n: ");
int n = int.Parse(Console.ReadLine()!);

int[,] array = GetArray(m, n, 0, 100);

PrintArray(array);
Sort(array);
